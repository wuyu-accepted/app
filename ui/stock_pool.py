def get_all_codes():
    """返回所有股票代码列表"""
    codes = []
    for sector_stocks in STOCK_POOL.values():
        codes.extend(sector_stocks.keys())
    return codes


def get_all_stocks_flat():
    """返回 [(code, name, sector), ...] 的扁平列表"""
    result = []
    for sector, stocks in STOCK_POOL.items():
        for code, name in stocks.items():
            result.append((code, name, sector))
    return result


def get_sector_of(code):
    """根据代码返回所属环节"""
    for sector, stocks in STOCK_POOL.items():
        if code in stocks:
            return sector
    return None
